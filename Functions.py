def export_message(files, message, dir=None):
    if files:
        if not dir:
            exported_dir = os.path.dirname(files[0])
        else:
            exported_dir = dir

        def open_directory():
            os.startfile(exported_dir)

        custom_box = CustomMessageBox("Success", message, f"{exported_dir}", open_directory)
        custom_box.show()

